int atoi(char *str)
{
	int i;
	int nb;
	int sign;

	i = 0;
	sign = 1;
	nb = 0;
	while( str[i] ==  ' ' || str[i]== '\n' || str[i] == '\t' || str[i] == '\v' || str[i] == '\r' || str[i] == '\f')
	{
		i++;
	}
	if (str[i] == '-')
		sign = -1;
	 if (str[i] == '-' || str[i] == '+')
			i++;
	while (str[i] >= '0' && str[i]  <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * sign);
}