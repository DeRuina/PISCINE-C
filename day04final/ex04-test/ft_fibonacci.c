int	ft_fibonacci(int index)
{
	int sum;
	if (index < 0)
	{
	return (-1);
	}
	else if (index == 0)
	{
	return (0);
	}
	else if (index == 1 || index == 2)
	{
	return (1);
	}
	{
	sum = 0;
	sum = sum + (sum + 1);
	}
}
